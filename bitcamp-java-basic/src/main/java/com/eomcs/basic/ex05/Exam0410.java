package com.eomcs.basic.ex05;

public class Exam0410 {
  public static void main(String[] args) {
    int i = 1;
    
    System.out.println(i << 1);
    
    System.out.println(i << 2);
    
    System.out.println(i << 3);
    
    System.out.println(i << 4);
    
    i = 11;
    
    System.out.println(i << 1);
    System.out.println(i << 2);
    System.out.println(i << 3);
  }

}
